# Los iteradores corren un determinado numero de veces una pieza de codigo

meses = ['octubre', 'febrero', 'marzo']

# Ciclo for
for mes in meses:
    print(mes)

print(f'Estoy viajando por el mundo en el mes de {mes}')

# Imprimir numeros
for numero in range(0, 20, 5):
    print(numero)

materias = ['matematica', 'fisica', 'quimica', 'logica']

for materia in materias:
    print(materia)

for numb in range(0, 101, 2):
    print(numb)
